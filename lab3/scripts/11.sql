-- Вивести рік виробництва, Назву інструмента, ціну, характеристики для інструментів,
-- які коштують в діапазоні 6200-8000 грн. та які не мають встановленої ціни.
-- Сортувати за назвою та ціною.
-- Примітка: умова "В діапазоні ТА не мають ціни" є взаємовиключною. Запит реалізовано з умовою "АБО".
SELECT
    year_of_production,
    name,
    price,
    characteristics
FROM Instrument
WHERE (price BETWEEN 6200 AND 8000) OR (price IS NULL)
ORDER BY name, price;
